package com.letsgotravel.myapp.domain;

public class ItineraryDetailsVo {

	private int ididx;
	private int tcidx;
	private String imap;
	private int nthDay;
	
	public int getIdidx() {
		return ididx;
	}
	public void setIdidx(int ididx) {
		this.ididx = ididx;
	}
	public int getTcidx() {
		return tcidx;
	}
	public void setTcidx(int tcidx) {
		this.tcidx = tcidx;
	}
	public String getImap() {
		return imap;
	}
	public void setImap(String imap) {
		this.imap = imap;
	}
	public int getNthDay() {
		return nthDay;
	}
	public void setNthDay(int nthDay) {
		this.nthDay = nthDay;
	}
}